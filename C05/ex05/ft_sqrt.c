int	ft_sqrt(int nb)
{
	int	i;
	int	result;

	result = 0;
	i = 1;
	if (nb <= 0)
		return (0);
	while (i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
